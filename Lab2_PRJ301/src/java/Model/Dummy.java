package Model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Dummy {
    private int did;
    private String dname;
    private Date dob;
    private boolean gender;
    private String username;

    public Dummy() {
    }

    public Dummy(int did, String dname, Date dob, boolean gender, String username) {
        this.did = did;
        this.dname = dname;
        this.dob = dob;
        this.gender = gender;
        this.username = username;
    }

    public int getDid() {
        return did;
    }

    public void setDid(int did) {
        this.did = did;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
    
    
}
